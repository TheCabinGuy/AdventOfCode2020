const recurringWidth = dataInput[0].length - 1;
function calculateHashesEncountered(data, xMovement, yMovement, recurringWidth){
  let x = 0;
  let y = 0;
  let hashesEncountered = 0
  while(y < data.length){
    if(data[y][x] === "#"){
      hashesEncountered+=1;
    }
    x = x + xMovement > recurringWidth ? (x+xMovement-1)-recurringWidth : x + xMovement;
    y+=yMovement;
  }
  return hashesEncountered;
}
